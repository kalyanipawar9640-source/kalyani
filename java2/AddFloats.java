public class AddFloats {
    public static void main (String[]args) {
        Float num1=12.5f;
        Float num2=7.3f;

        //Add two numbers
        Float sum = num1 + num2;

        //print result
        System.out.println("First number:"+num1);
        System.out.println("Second number:"+num2);
        System.out.println("sum:"+sum);
    }   
}